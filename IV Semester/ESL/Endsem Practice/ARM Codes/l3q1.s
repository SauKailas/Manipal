	AREA RESET, DATA, READONLY
	EXPORT _Vectors
_Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN 
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =N1
	LDR R1, =N2
	LDR R5, =DST
	MOV R2, #0
	LDR R3, [R0]
	LDR R4, [R1]
UP	CMP R3, R4
	BCC DOWN
	ADD R2, R2, #1
	SUBS R3, R3, R4
	B UP
DOWN STR R2, [R5]
STOP B STOP
N1 DCD 15
N2 DCD 5
	AREA mydata, DATA, READWRITE
DST DCD 0
	END