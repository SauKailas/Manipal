	AREA RESET, DATA, READONLY
	EXPORT _Vectors
_Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =N1
	LDR R1, [R0]
	LDR R2, =N2
	LDR R3, [R2]
	STM R13, {R1, R3}
	SUB R13, R13, #8
	ADD R13, R13, #8
	LDM R13, {R4, R5}
STOP B STOP
N1 DCD 1
N2 DCD 2
	AREA mydata, DATA, READWRITE
DST DCD 0
	END