	AREA RESET, DATA, READONLY
	EXPORT _Vectors
_Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =N1
	LDR R1, [R0]
	LDR R2, =N2
	LDR R3, [R2]
	MOV R4, #0
	MOV R5, #4
	LDR R6, =DST
UP	SUBS R1, R1, R3
	ADC R4, #-1
	STR R1, [R6], #4
	LDR R1, [R0, #4]!
	LDR R3, [R2, #4]!
	SUB R5, R5, #1
	CMP R5, #0
	BNE UP
	STR R4, [R6]
STOP B STOP
N1 DCD 0x40000000, 0x30000000, 0x20000000, 0x10000000
N2 DCD 0x20000000, 0x10000000, 0x00000000, 0x00000000
	AREA mydata, DATA, READWRITE
DST DCD 0
	END