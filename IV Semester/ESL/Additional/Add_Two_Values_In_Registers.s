	AREA RESET, DATA, READONLY
	EXPORT _Vectors
_Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN 
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =N1
	LDR R1, =N2
	LDR R4, =DST
	LDR R2, [R0]
	LDR R3, [R1]
	ADD R5, R2, R3
	STR R5, [R4]
STOP B STOP
N1 DCD 1
N2 DCD 2
	AREA mydata, DATA, READWRITE
DST DCD 0
	END